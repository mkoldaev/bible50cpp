int getcount(const char* a) {
    DIR *dp;
    int i = -2; //исключается метка текущего каталога и верхний уровень
    struct dirent *ep;
    dp = opendir(a);
    if (dp != NULL)
    {
        while (ep = readdir (dp))
            i++;
        (void) closedir (dp);
    }
    return i;
}

